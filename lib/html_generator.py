"""HTML report generator for RSS content."""

from datetime import datetime
from typing import List, Dict
import html


class HTMLReportGenerator:
    """Generate HTML reports from RSS feeds."""

    def __init__(self, title: str = "RSS Report"):
        self.title = title

    def generate(self, items: List[Dict], theme: str = "default") -> str:
        """Generate HTML report from items."""
        css = self._get_css(theme)

        html_content = f"""<!DOCTYPE html>
<html lang="zh-CN">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{html.escape(self.title)}</title>
    <style>{css}</style>
</head>
<body>
    <div class="container">
        <header>
            <h1>📰 {html.escape(self.title)}</h1>
            <p class="meta">Generated at {datetime.now().strftime('%Y-%m-%d %H:%M')}</p>
        </header>
        <main>
            {self._generate_items_html(items)}
        </main>
        <footer>
            <p>Generated by RSS Manager</p>
        </footer>
    </div>
</body>
</html>"""
        return html_content

